#Comparision returns boolean values
a = 9
b = 5
c = 9
#"==" operator
print(a == b)
print(a == c,"\n")

#"!=" operator
print(a != b)
print(a != c,"\n")

#greater than operator
print(a > b)
print(b > a,"\n")

#less than operator
print(a < b)
print(b < a,"\n")

#Greater than or Equal to
print(a >= b)
print(a >= c)
print(b >= a,"\n")

#less than or equal to
print(a <= b)
print(a <= c)
print(b <= a,"\n")


