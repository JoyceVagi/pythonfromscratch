#bitwise operators are used to perform bitwise calculations on integers. The integers are first converted into binary and then operations are performed on each bit or corresponding pair of bits, hence the name bitwise operators. 

#Bitwise AND 
a = 10
b = 4
print("a & b =", a & b)

#Bitwise OR
a = 10
b = 4
print("a | b =", a | b)

#Bitwise XOR
a = 10
b = 4
print("a ^ b =", a ^ b)

#Bitwise NOT
a = 10
b = 4
print("~a =", ~a)

#Bitwise right shift 
a = 10
b = -10
print("a >> 1 =", a >> 1)
print("b >> 1 =", b >> 1)

#Bitwise left shift
a = 5
b = -10
print("a << 1 =", a << 1)
print("b << 1 =", b << 1)



