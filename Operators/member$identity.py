#membership operators test for the membership of an object in a sequence, such as strings, lists, or tuples. They return boolean outputs

#Python IN operator:used to check if the character/element exists in the sequence or not 
#initializing a few sequences 
list = [1,2,3,4]
string = "Hello World"
dict = {1:"Python", 2:"is", 3:"cool!"}
#using the membership operator in
print(2 in list)#checking if 2 is in the list 
print("0" in string)#checking if 0 is in the string 
print(3 in dict)#checking if the word "cool" is in the dict 

